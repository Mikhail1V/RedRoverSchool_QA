# Задание 2.3
# Напишите программу, которая проверяет является ли год високосным.
# Таковыми считаются года, которые делятся без остатка на 4 (2004, 2008) и не являются столетиями (500, 600).
# Однако, если год делится без остатка  на 400, он также считается високосным (1200, 2000)

# Запрашиваем у пользователя год
year = int(input("Введите год: "))

# Проверяем, делится ли год на 4 без остатка
if year % 4 == 0:
    # Если да, то проверяем, является ли год столетием
    if year % 100 == 0:
        # Если да, то проверяем, делится ли год на 400 без остатка
        if year % 400 == 0:
            # Если да, то год високосный
            print("Год високосный")
        else:
            # Если нет, то год не високосный
            print("Год не високосный")
    else:
        # Если нет, то год високосный
        print("Год високосный")
else:
    # Если нет, то год не високосный
    print("Год не високосный")

# Задание 2.4
