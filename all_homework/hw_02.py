# Текст и количество повторений нужно ввести с помощью input()

# Запрашиваем у пользователя текст
text = input("Введите текст: ")

# Запрашиваем у пользователя количество повторений
repeats = int(input("Введите количество повторений: "))

# Печатаем текст заданное количество раз, построчно
for i in range(repeats):
    print(text)

# Задание 2.5.
# Напишите программу-калькулятор, которая принимает два числа и оператор (в формате str),
# производит заданное арифметическое действие и
# печатает результат в формате: {num1} {operator) {num2) = {result}

# Запрашиваем у пользователя два числа и оператор
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))
operator = input("Введите оператор (+, -, *, /): ")

# Производим заданное арифметическое действие в зависимости от оператора
if operator == "+":
    result = num1 + num2
elif operator == "-":
    result = num1 - num2
elif operator == "*":
    result = num1 * num2
elif operator == "/":
    result = num1 / num2
else:
    result = "Неверный оператор"

# Печатаем результат в формате: {num1} {operator) {num2) = {result}
print(f"{num1} {operator} {num2} = {result}")